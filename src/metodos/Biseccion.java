    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
Funciones f = new Funciones();
        DecimalFormat formato = new DecimalFormat("#.####");


//DECLARACIÃ“N DE VARIABLES
        double variablea = Double.parseDouble(a.getText());
        double variableb = Double.parseDouble(b.getText());
        double tol1 = Double.parseDouble(tolerancia.getText());
        double fXr = 0;
        double fxanterior = 0;
        int iteraciones = 1;

        //INICIO DE METODOS
        do {
            fXr = f.xr(variablea, variableb);
            double fa = f.intevaloA(variablea);
            double fb = f.intevaloB(fXr);
            double Xr = f.fxr(fXr);
            double tol = Math.abs(fxanterior - Xr);

            if (fa * fb < 0) {
                variableb = fXr;
            } else {
                variablea = fXr;
            }

            String aconvertido = formato.format(variablea);
            String bconvertido = formato.format(variableb);
            String faconvertido = formato.format(fa);
            String fbconvertido = formato.format(fb);
            String xrconvertido = formato.format(fXr);
            String fxrconvertido = formato.format(Xr);
            String tolconvertido = formato.format(tol);

            modelo2.addRow(new Object[]{
                iteraciones,
                aconvertido,
                bconvertido,
                faconvertido,
                fbconvertido,
                xrconvertido,
                fxrconvertido,
                tolconvertido
            });


            /* System.out.println("Iteracion: " + iteraciones + " a: " + aconvertido + " b: " + bconvertido + " f(a): " + faconvertido + " f(b): " + fbconvertido + " xr: " + fxrconvertido + " f(xr): " + xrconvertido + " Tolerancia:"+tolconvertido);
             */
            iteraciones++;
            fxanterior = Xr;
        } while (Math.abs(variableb - variablea) > tol1);
   
    }//GEN-LAST:event_jButton1ActionPerformed

